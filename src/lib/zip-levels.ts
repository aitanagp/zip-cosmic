export interface ZipLevel {
  id: number;
  name: string;
  width: number;
  height: number;
  difficulty: 'fácil' | 'medio' | 'difícil';
  grid: {
    type: 'empty' | 'obstacle' | 'number';
    numberValue?: number; // 1, 2, 3, etc.
  }[][];
  solution: [number, number][]; // Pre-calculated path coordinates [row, col]
}

// Helper to compile text-based layouts into ZipLevel format
function createLevel(
  id: number,
  name: string,
  difficulty: 'fácil' | 'medio' | 'difícil',
  width: number,
  height: number,
  layout: string[],
  solution: [number, number][]
): ZipLevel {
  const grid = layout.map(rowStr => {
    const tokens = rowStr.trim().split(/\s+/);
    return tokens.map(token => {
      if (token === '.') {
        return { type: 'empty' as const };
      } else if (token === '#') {
        return { type: 'obstacle' as const };
      } else {
        const val = parseInt(token, 10);
        return { type: 'number' as const, numberValue: val };
      }
    });
  });

  return {
    id,
    name,
    difficulty,
    width,
    height,
    grid,
    solution
  };
}

